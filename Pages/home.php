			spaceBetween: 30,
			centeredSlides: false,
			loop: true,
			autoplay: {
				delay: 2500,
				disableOnInteraction: false,
			},
			breakpoints: {
				640: {
					slidesPerView: 3,
				},
				768: {
					slidesPerView: 5,
				},
				1024: {
					slidesPerView: 8,
				},
			}
		});
		
		// Parallax 3D effect for cards
		document.addEventListener('DOMContentLoaded', function() {
			const cards = document.querySelectorAll('.step-card');
			
			cards.forEach(card => {
				let shine = card.querySelector('.step-card-shine');
				let bounds = {};
				
				function handleMouseMove(e) {
					// Add active class to remove transitions during mouse movement
					card.classList.add('animation-active');
					
					// Get position of mouse over card
					const rect = card.getBoundingClientRect();
					const mouseX = e.clientX - rect.left;
					const mouseY = e.clientY - rect.top;
					
					// Calculate distance from center in percentages
					const centerX = rect.width / 2;
					const centerY = rect.height / 2;
					const percentX = (mouseX - centerX) / centerX;
					const percentY = (mouseY - centerY) / centerY;
					
					// Limit rotation (10 degrees)
					const maxRotation = 12;
					const rotateX = -percentY * maxRotation;
					const rotateY = percentX * maxRotation;
					
					// Apply transform to card
					card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.02, 1.02, 1.02)`;
					
					// Update shine position
					if (shine) {
						// Calculate shine position
						const shineX = mouseX / rect.width * 100;
						const shineY = mouseY / rect.height * 100;
						shine.style.background = `radial-gradient(circle at ${shineX}% ${shineY}%, rgba(255,255,255,0.2) 0%, rgba(255,255,255,0) 60%)`;
						shine.style.opacity = '1';
					}
					
					// Depth effect for inner elements
					const innerElements = card.querySelectorAll('div:not(.step-card-shine), h3, p, svg');
					const maxDepth = 30;
					innerElements.forEach((el, index) => {
						// Different depth for different elements
						const depth = maxDepth - (index * 5);
						const moveX = percentX * depth;
						const moveY = percentY * depth;
						
						// Apply transform to inner elements
						el.style.transform = `translate3d(${moveX}px, ${moveY}px, 0)`;
					});
				}
				
				function handleMouseLeave() {
					// Reset card transform
					card.style.transform = '';
					card.classList.remove('animation-active');
					
					// Reset shine
					if (shine) {
						shine.style.opacity = '0';
					}
					
					// Reset inner elements
					const innerElements = card.querySelectorAll('div:not(.step-card-shine), h3, p, svg');
					innerElements.forEach(el => {
						el.style.transform = '';
					});
				}
				
				card.addEventListener('mousemove', handleMouseMove);
				card.addEventListener('mouseleave', handleMouseLeave);
				card.addEventListener('touchend', handleMouseLeave);
				
				// Add shadow on hover
				card.addEventListener('mouseenter', function() {
					card.style.boxShadow = '0 25px 30px rgba(0, 0, 0, 0.3)';
				});
				
				card.addEventListener('mouseleave', function() {
					card.style.boxShadow = '0 4px 8px rgba(0, 0, 0, 0.15)';
				});
			});
		});
	</script>
